%-------------------------------------------------------------------------
% Startup Folder When Starting the MATLAB Program from a Windows Shortcut
%-------------------------------------------------------------------------
% 
% When you start the MATLAB program from a Windows shortcut, by default MATLAB
% sets the startup folder to the userpath value, whose default value is My
% Documents\MATLAB, or Documents\MATLAB on Windows Vista platforms. The userpath
% folder is automatically added to the search path.
%
% If there is a value specified in the Start in field of the Properties dialog
% box for the MATLAB program, that value is the startup folder, although the
% userpath is added to the search path. If MATLAB does not find a valid userpath
% value upon startup, and the Start in field is empty, the startup folder is the
% Windows desktop.
%
%
%-------------------------------------------------------------------------
% Startup Folder When Starting the MATLAB Program from an Associated File
%-------------------------------------------------------------------------
% When you start MATLAB by double-clicking a file type associated with MATLAB,
% that file's folder is the startup folder. The userpath folder is automatically
% added to the search path.
%
%
%-------------------------------------------------------------------
% Startup Folder When Starting the MATLAB Program from a DOS Window
%-------------------------------------------------------------------
% When you start MATLAB from a DOS window, the startup folder is the folder in
% which you ran the matlab function. The userpath folder is automatically added
% to the search path.
%
